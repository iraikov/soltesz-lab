        <h1>Acorn Woodpecker</h1>
        <p>The <strong>Acorn Woodpecker</strong> (<i>Melanerpes formicivorus</i>) is a medium-sized <a href='http://en.wikipedia.org/wiki/Woodpecker' target='_blank'>woodpecker</a>, 21 cm long with an average weight of 85 g.</p>

		<p>The adult has a black head, back, wings and tail, white forehead, throat, belly and rump. The eyes are white. The adult male has a red cap starting at the forehead, whereas females have a black area between the forehead and the cap. The white neck, throat and forehead patches are distinctive identifiers.</p>
